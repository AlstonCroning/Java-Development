package com.AlstonCroning;

public class Dimensions {
    private int width;
    private int lenght;
    private int depth;

    public Dimensions(int width, int lenght, int depth) {
        this.width = width;
        this.lenght = lenght;
        this.depth = depth;
    }

    public int getWidth() {
        return width;
    }

    public int getLenght() {
        return lenght;
    }

    public int getDepth() {
        return depth;
    }
}
